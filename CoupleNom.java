package miniprojet;

public class CoupleNom {
    private Nom nom1;
    private Nom nom2;

    public CoupleNom(Nom nom1, Nom nom2) {
        this.nom1 = nom1;
        this.nom2 = nom2;
    }

    public Nom getNom1() {
        return nom1;
    }

    public Nom getNom2() {
        return nom2;
    }
}
